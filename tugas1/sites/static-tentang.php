<h3>Why PHP <small>Alasan memilih PHP</small></h3>
<p align="justify">PHP merupakan bahasa pemrograman
	yang saat ini sangat popular dikalangan
	programmer di seluruh dunia. Bahasa programram yang diperuntukan bagi pengembangan
	aplikasi berbasis web ini sudah sangat banyak penggunanya karena kehandalan serta
	mudah diimplementasikan di banyak server.</p>
<p align="justify">Beberapa alasan saya menggunakan php antara lain :</p>
<ol>
	<li><strong>Gratis</strong><br>
	Gratis..???! Ya Gratis!, PHP merupakan bahasa yang gratis. Artinya kita tidak 
	perlu membayar lisensi ketika akan menggunakan untuk mengembangkan aplikasi
	menggunakan bahasa ini.</li>
	<li><strong>Multi Platform OS</strong><br>
	Artinya, aplikasi yang ditulis dengan bahasa pemrograman php dapat dijalankan
	di berbagai platform Sistem Operasi. Php dapat dijalankan di sistem operasi
	berbasis Unix <em>(unix like operating system)</em>, seperti <a href="http://id.wikipedia.org/wiki/Linux">
	GNU/Linux</a>, <a href="http://www.apple.com">Machintosh</a>, BSD Family, dan lain-lain,
	bahkan php dapat dijalankan di sistem operasi yang berbasis <a href="http://www.microsoft.com">
	Windows</a>. Php juga dapat langsung dijalankan melalui shell (bersifat interpreter)
	maupun dijalankan melalui webserver untuk membuat aplikasi berbasis web seperti
	aplikasi yang sedang ada di hadapan Anda ini.</li>
	<li><strong>Mendukung banyak database</strong><br>
	Kelebihannya yang satu ini menjadikan PHP sejajar dengan bahasa yang sudah ada
	sebelumnya. Database yang dapat didudukung oleh PHP antara lain : SQL Server,
	Access, Adabas D, dBase, Empress, FilePro (read-only) FilePro (read-only)
	Hyperwave, IBM DB2, Informix, Ingres, InterBase, FrontBase mSQL, Direct MS-
	SQL, MySQL MySQL, ODBC, Oracle (OCI7 and OCI8), Ovrimos, PostgreSQL
	SQLite, Solid, Sybase, Velocis, Unix dbm.</li>
	<li><strong>Embeded Script</strong><br>
	Embed Script maksudnya adalah kita dapat meng-embed atau menempelkan script
	PHP pada script bahasa pemrograman yang lainnya dengan menuliskan tag
	pembuka dan penutup script PHP.</li>
</ol><br>
